package com.example.learningmaps;

public class AddingItemsBHero {

    private String bheroname;
    private String bheroimageURL;

    public AddingItemsBHero(String bheroname, String bheroimageURL) {
        this.bheroname = bheroname;
        this.bheroimageURL = bheroimageURL;
    }

    public String getBheroname() {
        return bheroname;
    }

    public String getBheroimageURL() {
        return bheroimageURL;
    }
}
